public class Animal
{
    //Instance Variables
    public String sound;
    public String type;

    //Constructor
    public Animal(String sound, String type)
    {
        this.sound = sound;
        this.type = type;
    }

    //Getters
    public String getSound()
    {
        return sound;
    }
    public String getType()
    {
        return type;
    }
}
